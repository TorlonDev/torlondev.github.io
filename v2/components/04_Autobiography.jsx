        <li>24. ตั้งแต่เรียนจบ มีรายได้ เวลาผมไปหายายที่อ้อมน้อย ผมเอาเงินไปให้ตลอดหลายครั้ง พอกลับมา ไม่ถึง 2 อาทิตย์ ก็โทรมาหาผมอีก ถามว่าเมื่อไหร่จะไปหา ทั้งที่ผมเพิ่งไปหา เอาเงินไปให้ และชอบโทรหาผม ตอน 10 โมง ซึ่งมันเป็นเวลาทำงาน เขาโทรหาผมเวลาทำงานบ่อยมาก ถ้าไม่รับ ก็จะโทรมารัวๆ พอบอกว่า ไม่ให้โทรมาเวลาทำงาน เขาก็จะหายไปช่วงนึง พอนานวันเข้าก็กลับมาโทรเวลาทำงานอีก (กลัวมีอะไรฉุกเฉินเลยทำอะไรไม่ได้)</li>
