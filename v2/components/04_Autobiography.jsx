        <li>33. มีอยู่วันนึง ป้าเล็กเขาน้ำตาลลด นอนชักกับพื้น แล้วต้องเรียกรถพยาบาล ผมโทรเรียกรถพยาบาล และผมก็นั่งรถพยาบาลไปด้วย ตอนเขานอนเปลขึ้นรถพยาบาล ไม่ได้หยิบรองเท้าแตะไปด้วย เพราะรีบกันมาก ซึ่งเขาต้องนอนโรงพยาบาล พอวันต่อมา ผมเอารองเท้าแตะไปให้เขา เขาเขวี้ยงรองเท้า แล้วตะคอกใส่ผม ว่ามารับทำไม ต่อหน้าพยาบาล เยอะๆ เขาทำแบบนั้นไปทำไม นี่คือปมชีวิตที่ใหญ่มากของผม ผมอดทนมากๆวันนั้น</li>
