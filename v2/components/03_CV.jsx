const onDoubleClickImg = (idImg) => {
  return () => {
    if ($(`#${idImg}`).hasClass('img_active')) {
      $(`#${idImg}`).removeClass('img_active')
      return
    }
    if (!$(`#${idImg}`).hasClass('img_active')) {
      $(`#${idImg}`).addClass('img_active')
    }
  }
}

